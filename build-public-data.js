/* Predict2U v264 — builds a small rolling public data bundle from data.js. */
'use strict';
const fs=require('fs');
const path=require('path');
const ROOT=__dirname;
const source=path.join(ROOT,'data.js');
const target=path.join(ROOT,'current-data.js');
const metaTarget=path.join(ROOT,'data-meta.json');
function read(){
  const raw=fs.readFileSync(source,'utf8');
  const match=raw.match(/window\.MATCHES\s*=\s*(\[[\s\S]*\])\s*;?\s*$/);
  if(!match)throw new Error('data.js does not contain window.MATCHES.');
  const updated=(raw.match(/window\.DATA_UPDATED\s*=\s*["']([^"']+)["']/)||[])[1]||null;
  return {matches:JSON.parse(match[1]),updated};
}
const iso=d=>d.toISOString().slice(0,10);
const add=(d,n)=>{const x=new Date(d);x.setUTCDate(x.getUTCDate()+n);return x;};
const dateOf=m=>String(m&&m.matchDate||m&&m.kickoff||'').slice(0,10);
function run(now=new Date()){
  const {matches,updated}=read();
  const today=iso(now);
  const from=iso(add(new Date(today+'T00:00:00Z'),-7));
  const to=iso(add(new Date(today+'T00:00:00Z'),7));
  const dates=matches.map(dateOf).filter(Boolean).sort();
  const maxFixtureDate=dates[dates.length-1]||null;
  let rows=matches.filter(m=>{const d=dateOf(m);return d&&d>=from&&d<=to;});
  let fallback=false;
  if(!rows.length&&maxFixtureDate){
    fallback=true;
    const max=new Date(maxFixtureDate+'T00:00:00Z');
    const fallbackFrom=iso(add(max,-7));
    rows=matches.filter(m=>{const d=dateOf(m);return d&&d>=fallbackFrom&&d<=maxFixtureDate;});
  }
  rows.sort((a,b)=>dateOf(a).localeCompare(dateOf(b))||String(a.kickoff||'').localeCompare(String(b.kickoff||'')));
  const meta={
    version:'v264',generatedAt:new Date().toISOString(),sourceUpdatedAt:updated,
    today,window:{from,to},fallback,maxFixtureDate,totalSourceMatches:matches.length,publicMatches:rows.length
  };
  fs.writeFileSync(target,`/* AUTO-GENERATED by build-public-data.js — do not edit. */\nwindow.P2U_DATA_META=${JSON.stringify(meta)};\nwindow.DATA_UPDATED=${JSON.stringify(updated||meta.generatedAt)};\nwindow.MATCHES=${JSON.stringify(rows)};\n`,'utf8');
  fs.writeFileSync(metaTarget,JSON.stringify(meta,null,2)+'\n','utf8');
  console.log(`Public data: ${rows.length}/${matches.length} matches, ${(fs.statSync(target).size/1024/1024).toFixed(2)} MB.`);
  return meta;
}
if(require.main===module)run();
module.exports={run};
