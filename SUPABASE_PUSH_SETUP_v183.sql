-- Optional automatic followed-user posts. This trigger is installed only when
-- the existing public.slips table is present.
create or replace function public.p2u_push_from_public_slip()
returns trigger
language plpgsql
security definer
set search_path=public
as $$
declare payload jsonb:=to_jsonb(new);
declare author_id uuid;
declare handle text;
declare public_post boolean;
begin
  public_post:=lower(coalesce(payload->>'is_public','false')) in ('true','t','1','yes');
  begin author_id:=nullif(payload->>'user_id','')::uuid; exception when others then author_id:=null; end;
  handle:=left(coalesce(nullif(payload->>'handle',''),nullif(payload->>'user_handle',''),'Community member'),50);
  if public_post and author_id is not null then
    insert into public.p2u_push_jobs(category,title,body,url,audience,payload,requested_by)
    values('community',handle||' posted a Community record','A member you follow shared a new public record.','community.html',
      jsonb_build_object('type','followed_user','author_user_id',author_id::text),
      jsonb_build_object('source','slips','slip_id',coalesce(payload->>'id',''),'author_user_id',author_id::text),author_id);
  end if;
  return new;
end;
$$;

do $$
begin
  if to_regclass('public.slips') is not null then
    execute 'drop trigger if exists p2u_push_public_slip_trigger on public.slips';
    execute 'create trigger p2u_push_public_slip_trigger after insert on public.slips for each row execute function public.p2u_push_from_public_slip()';
  end if;
end $$;

